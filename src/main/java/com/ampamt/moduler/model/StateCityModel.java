package com.ampamt.moduler.model;

import java.util.List;

import com.ampamt.moduler.bean.location.TblCityBean;

public class StateCityModel {

	private String stateName;
	private String stateCode;
	private List<TblCityBean> tblCityBean;
	public String getStateName() {
		return stateName;
	}
	public void setStateName(String stateName) {
		this.stateName = stateName;
	}
	public String getStateCode() {
		return stateCode;
	}
	public void setStateCode(String stateCode) {
		this.stateCode = stateCode;
	}
	public List<TblCityBean> getTblCityBean() {
		return tblCityBean;
	}
	public void setTblCityBean(List<TblCityBean> tblCityBean) {
		this.tblCityBean = tblCityBean;
	}
	
	
}
