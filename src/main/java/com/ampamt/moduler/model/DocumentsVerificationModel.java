package com.ampamt.moduler.model;

public class DocumentsVerificationModel {

	private String id;
	private String allDocUploaded;
	private String accountType;
	private String availableAmount;
	private String isLocationSelected;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAllDocUploaded() {
		return allDocUploaded;
	}
	public void setAllDocUploaded(String allDocUploaded) {
		this.allDocUploaded = allDocUploaded;
	}
	public String getAccountType() {
		return accountType;
	}
	public void setAccountType(String accountType) {
		this.accountType = accountType;
	}
	public String getAvailableAmount() {
		return availableAmount;
	}
	public void setAvailableAmount(String availableAmount) {
		this.availableAmount = availableAmount;
	}
	public String getIsLocationSelected() {
		return isLocationSelected;
	}
	public void setIsLocationSelected(String isLocationSelected) {
		this.isLocationSelected = isLocationSelected;
	}

	
}
