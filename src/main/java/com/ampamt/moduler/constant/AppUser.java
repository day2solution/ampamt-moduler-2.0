package com.ampamt.moduler.constant;

public class AppUser {

    /**
     * ApplicationUser's user value
     */
    private ApplicationUser user;
    /**
     * IfaMember.ID / IamId.ID
     */
    private String id;
    /**
     * IamAccount.REFNO
     */
    private String refno;

    public AppUser() {
    }

    public AppUser(ApplicationUser user, String id, String refno) {
        this.user = user;
        this.id = id;
        this.refno = refno;
    }

    public ApplicationUser getUser() {
        return user;
    }

    public void setUser(ApplicationUser user) {
        this.user = user;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRefno() {
        return refno;
    }

    public void setRefno(String refno) {
        this.refno = refno;
    }

}
